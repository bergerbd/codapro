package de.codapro.tdms.model;

/**
 * A typed value that can be used for properties to force certain types instead
 *   of guessing a matching one.
 */
public final class TypedValue {
	private final Type type;
	private final Object value;

	public TypedValue(final Type type, final Object value) {
		this.type = type;
		this.value = value;
	}

	public Type getType() {
		return type;
	}

	public Object getValue() {
		return value;
	}
}
